function filter(element) 
{
        
	var value = $(element).val();

	$(".icon").each(function() {
		if ($(this).find('small').search(value) > -1) 
		{
			$(this).parent().show();
		}
		else 
		{
			$(this).parent().hide();
		}
	});
}  


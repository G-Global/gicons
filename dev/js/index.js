/*
*
*	@Create Icon Sections
*
*
*/
function mixAll( iconClass , cssContent )
{	
	var allIcons = [];
	var allObj = {};
	var Arraylenght = iconClass.length;
	
	var keys = Object.keys(SECTIONS);

	for(var i = 0; i < Arraylenght ; i++)
	{
		var name = iconClass[i];

		var iconObj = {
			className: iconClass[i],
			cssContent:cssContent[i]
		};
		
		for(var j = 0; j < keys.length; j++)
		{
			if(name.indexOf(keys[j]) !== -1)
			{
				SECTIONS[keys[j]].push(iconObj)
			}
		}
		//allIcons.push('.#{$g-css-prefix}-'+iconClass[i]+':before { content:$g-var-'+iconClass[i]+';}<br>');
	}
	// console.log('AllIcons=>>>>',allIcons);
	// $('#iconSASS').html(allIcons)
	printToDOM(SECTIONS);
};
/*
*
*
*	@Print Icons to DOM	
*
*
*/
function printToDOM( data )
{
	var holder = $('#iconHolder');
	for(var i in data)
	{	
		var column = ''
		for(var j = 0 ; j < data[i].length; j++)
		{

			column += '<div class="col-xs-6 col-sm-3">'+
							'<div class="icon" data-className="'+ICON_PREFIX+data[i][j].className+'" data-cssContent="'+data[i][j].cssContent+'">'+
								'<i class="'+ICON_PREFIX+data[i][j].className+'"></i>&nbsp;'+
								'<small>'+removePrefix(ICON_PREFIX)+data[i][j].className+'</small>'+
							'</div>'+
					  '</div>';
		}
		var row = 	'<div class="row">'+
						'<h3 style="text-transform:capitalize;">'+i+'</h3>'+
						'<hr>'+
						'<div class="iconHolder">'+column+'</div>'+
					'</div>';
		holder.append(row);
	}
}
/*
*
*	@Remove PRefix
*
*
*/
function removePrefix(str)
{
	return str.replace('g ', '');
}
